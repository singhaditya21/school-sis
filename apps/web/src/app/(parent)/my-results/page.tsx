'use client';

import { useState, useEffect } from 'react';
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { getMyResults, MyResult } from '@/lib/services/parent/parent.service';
import { BarChart, Bar, XAxis, YAxis, CartesianGrid, Tooltip, ResponsiveContainer, Legend } from 'recharts';
import { Download, Printer, TrendingUp, Trophy } from 'lucide-react';
import jsPDF from 'jspdf';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';

export default function MyResultsPage() {
    const [results, setResults] = useState<MyResult[]>([]);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        getMyResults().then(r => {
            setResults(r);
            setLoading(false);
        });
    }, []);

    const grouped = results.reduce((acc: Record<string, MyResult[]>, r) => {
        const key = r.examName || 'Unknown Exam';
        (acc[key] = acc[key] || []).push(r);
        return acc;
    }, {});

    const generatePDF = (examName: string, subjects: MyResult[]) => {
        const doc = new jsPDF();
        doc.setFontSize(22);
        doc.text(`Official Result: ${examName}`, 14, 20);
        
        doc.setFontSize(12);
        doc.setTextColor(100);
        doc.text("Generated by ScholarMind SIS", 14, 30);
        
        let yPos = 45;
        doc.setTextColor(0);
        doc.setFontSize(10);
        doc.text("Subject", 14, yPos);
        doc.text("Marks Obtained", 80, yPos);
        doc.text("Total", 120, yPos);
        doc.text("%", 150, yPos);
        doc.text("Grade", 180, yPos);
        
        yPos += 5;
        doc.line(14, yPos, 195, yPos);
        yPos += 8;

        subjects.forEach(s => {
            doc.text(s.subject, 14, yPos);
            doc.text(s.marksObtained.toString(), 80, yPos);
            doc.text(s.totalMarks.toString(), 120, yPos);
            doc.text(s.percentage.toString() + "%", 150, yPos);
            doc.text(s.grade, 180, yPos);
            yPos += 10;
        });

        doc.save(`${examName}_Report_Card.pdf`);
    };

    if (loading) return <div className="p-8 text-center text-sm text-gray-500">Loading academic records...</div>;

    return (
        <div className="space-y-8 max-w-7xl mx-auto pb-12">
            <div className="flex flex-col md:flex-row justify-between items-start md:items-center border-b pb-6">
                <div>
                    <h1 className="text-2xl font-semibold tracking-tight text-slate-900">Academic Records</h1>
                    <p className="text-sm text-slate-500 mt-1">Detailed performance tracking and official transcripts</p>
                </div>
            </div>

            {Object.keys(grouped).length === 0 ? (
                <div className="border border-dashed rounded-lg p-12 text-center text-slate-500 bg-slate-50">
                    No academic records available for the current term.
                </div>
            ) : (
                Object.entries(grouped).map(([examName, subjects]) => {
                    const avg = subjects.length > 0 ? Math.round(subjects.reduce((sum, s) => sum + s.percentage, 0) / subjects.length) : 0;
                    
                    const chartData = subjects.map(s => ({
                        subject: s.subject,
                        percentage: s.percentage,
                    }));

                    return (
                        <Card key={examName} className="border-slate-200 shadow-sm rounded-md overflow-hidden">
                            <CardHeader className="bg-slate-50 border-b px-6 py-4 flex flex-row items-center justify-between">
                                <div>
                                    <CardTitle className="text-lg font-semibold text-slate-800">{examName}</CardTitle>
                                    <CardDescription className="text-xs font-medium mt-1">
                                        Term Final Assessment
                                    </CardDescription>
                                </div>
                                <div className="flex items-center gap-4">
                                    <Badge variant="secondary" className="px-3 py-1 font-mono text-sm border-slate-200">
                                        Average: {avg}%
                                    </Badge>
                                    <Button size="sm" variant="outline" className="h-8 text-xs font-medium" onClick={() => generatePDF(examName, subjects)}>
                                        <Download className="w-4 h-4 mr-2" /> Export PDF
                                    </Button>
                                </div>
                            </CardHeader>
                            <CardContent className="p-0">
                                <div className="grid lg:grid-cols-3 divide-y lg:divide-y-0 lg:divide-x border-b">
                                    {/* Data Table */}
                                    <div className="lg:col-span-2 overflow-x-auto p-0">
                                        <Table>
                                            <TableHeader className="bg-slate-50">
                                                <TableRow>
                                                    <TableHead className="font-medium text-slate-500">Student</TableHead>
                                                    <TableHead className="font-medium text-slate-500">Subject</TableHead>
                                                    <TableHead className="text-right font-medium text-slate-500">Score</TableHead>
                                                    <TableHead className="text-right font-medium text-slate-500">Percentage</TableHead>
                                                    <TableHead className="font-medium text-slate-500">Grade</TableHead>
                                                </TableRow>
                                            </TableHeader>
                                            <TableBody>
                                                {subjects.map((s, idx) => (
                                                    <TableRow key={idx} className="hover:bg-slate-50">
                                                        <TableCell className="font-medium text-slate-800">{s.studentName}</TableCell>
                                                        <TableCell className="font-medium text-slate-700">{s.subject}</TableCell>
                                                        <TableCell className="text-right font-mono text-slate-600">
                                                            {s.marksObtained} <span className="text-slate-400">/ {s.totalMarks}</span>
                                                        </TableCell>
                                                        <TableCell className="text-right font-mono font-medium text-slate-700">
                                                            {s.percentage}%
                                                        </TableCell>
                                                        <TableCell>
                                                            <Badge variant={s.percentage >= 80 ? 'default' : s.percentage >= 60 ? 'secondary' : 'destructive'} className="rounded-sm px-2 font-mono text-[10px]">
                                                                {s.grade || 'N/A'}
                                                            </Badge>
                                                        </TableCell>
                                                    </TableRow>
                                                ))}
                                            </TableBody>
                                        </Table>
                                    </div>
                                    
                                    {/* Analytics Sidebar */}
                                    <div className="p-6 bg-slate-50/30">
                                        <h3 className="text-xs font-semibold text-slate-500 uppercase tracking-wider mb-4 flex items-center">
                                            <TrendingUp className="w-4 h-4 mr-2" /> Performance Distribution
                                        </h3>
                                        <div className="h-[200px] w-full">
                                            <ResponsiveContainer width="100%" height="100%">
                                                <BarChart data={chartData} margin={{ top: 10, right: 10, left: -20, bottom: 0 }}>
                                                    <CartesianGrid strokeDasharray="3 3" vertical={false} stroke="#e2e8f0" />
                                                    <XAxis dataKey="subject" tick={{ fontSize: 10 }} tickLine={false} axisLine={false} />
                                                    <YAxis tick={{ fontSize: 10 }} tickLine={false} axisLine={false} domain={[0, 100]} />
                                                    <Tooltip cursor={{ fill: '#f1f5f9' }} contentStyle={{ borderRadius: '6px', fontSize: '12px', border: 'none', boxShadow: '0 4px 6px -1px rgb(0 0 0 / 0.1)' }} />
                                                    <Bar dataKey="percentage" fill="#0f172a" radius={[4, 4, 0, 0]} maxBarSize={40} />
                                                </BarChart>
                                            </ResponsiveContainer>
                                        </div>
                                    </div>
                                </div>
                            </CardContent>
                        </Card>
                    );
                })
            )}
        </div>
    );
}
